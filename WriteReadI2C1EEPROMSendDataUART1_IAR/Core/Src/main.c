void ByteWrite(uint8_t DevAddress, uint16_t address, uint8_t data)
{
  uint8_t X[3];
  DevAddress <<= 1;
  X[0] = (address >> 8);
  X[1] = address;
  X[2] = data;
  HAL_I2C_Master_Transmit(&hi2c1, DevAddress, X, 3, 10);
  HAL_Delay(5);
}

uint8_t RandomRead(uint8_t DevAddress, uint16_t address)
{
  uint8_t X[2];
  uint8_t read;
  DevAddress <<= 1;
  X[0] = (address >> 8);
  X[1] = address;
  HAL_I2C_Master_Transmit(&hi2c1, DevAddress, X, 2, 10);
  HAL_I2C_Master_Receive(&hi2c1, DevAddress, (uint8_t*) &read, 1, 10);
  HAL_Delay(5);
  return read;
}

